package com.inhatc.system.chart.vo;

public class ChartVO {
	private String research_classification;
	private String practice_classification;
	private String normal_classification;
	private String sum_classification;
	private String year;
	private String completedate;
private String instrument;
	
	
	public String getInstrument() {
	return instrument;
}
public void setInstrument(String instrument) {
	this.instrument = instrument;
}
	public String getCompletedate() {
		return completedate;
	}
	public void setCompletedate(String completedate) {
		this.completedate = completedate;
	}
	public String getResearch_classification() {
		return research_classification;
	}
	public void setResearch_classification(String research_classification) {
		this.research_classification = research_classification;
	}
	public String getPractice_classification() {
		return practice_classification;
	}
	public void setPractice_classification(String practice_classification) {
		this.practice_classification = practice_classification;
	}
	public String getNormal_classification() {
		return normal_classification;
	}
	public void setNormal_classification(String normal_classification) {
		this.normal_classification = normal_classification;
	}
	public String getSum_classification() {
		return sum_classification;
	}
	public void setSum_classification(String sum_classification) {
		this.sum_classification = sum_classification;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	
	
}
